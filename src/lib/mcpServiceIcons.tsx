/**
 * Get the icon component for a given MCP service name
 * @param serviceName - The name of the MCP service
 * @param size - Optional size for the icon (default: 20)
 * @param className - Optional CSS class for styling
 * @returns React.ReactNode - The icon component or fallback
 */
export function getMCPServiceIcon(
  serviceName: string,
  size: number = 20,
  className: string = ''
): React.ReactNode {
  if (!serviceName) {
    return <Wrench size={size} className={className} />;
  }

  const normalizedName = serviceName.toLowerCase().trim();

  // Try exact match first
  if (mcpServiceIconMap[normalizedName]) {
    const IconComponent = mcpServiceIconMap[normalizedName];
    return <IconComponent size={size} className={className} />;
  }

  // Try case-insensitive exact match
  const matchingKey = Object.keys(mcpServiceIconMap).find(
    (key) => key.toLowerCase() === normalizedName
  );

  if (matchingKey) {
    const IconComponent = mcpServiceIconMap[matchingKey];
    return <IconComponent size={size} className={className} />;
  }

  // Fallback to Wrench icon
  return <Wrench size={size} className={className} />;
}
